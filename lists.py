nums = [1,2,3,4,5]
print(nums)
# [1,2,3,4,5]

fruits = ['banana', 'apple', 'orange']
print(fruits)
# ['banana', 'apple', 'orange']

print(fruits[1])
# apple

fruits.append('grape')
print(fruits)
# ['banana', 'apple', 'orange', 'grape']

fruits.remove('apple')
print(fruits)
# ['banana', 'orange', 'grape']